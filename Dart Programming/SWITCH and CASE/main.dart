void main(List<String> args) {
  // switch case statement

  // ignore: unused_local_variable
  String grade = "No";

  switch (grade) {
    case "A":
      print("Excellent grade of");
      break;
    case "B":
      print("very Good");
      break;
    case "C":
      print("Good enough");
      break;
    case "S":
      print("Work hard");
      break;
    case "F":
      print("You are fail");
      break;
    default:
      print("You Input Wrong");
  }
}
